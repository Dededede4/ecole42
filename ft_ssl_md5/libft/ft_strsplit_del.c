#include "libft.h"

void		ft_strsplit_del(char ***str)
{
	int		i;
	char	**s;

	i = 0;
	s = *str;
	while (s[i])
	{
		ft_strdel(s + i);
		i++;
	}
	ft_memdel((void**)&s);
}
